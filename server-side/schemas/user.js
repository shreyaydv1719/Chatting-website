const mongoose = require("mongoose");

const userschema = mongoose.Schema({
  fullname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  token: {
    type: String,
  },
});

const Users = mongoose.model("user", userschema);

module.exports = Users;
